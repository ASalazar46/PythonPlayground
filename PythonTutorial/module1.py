def squareMe(n):
    return n ** 2

def exponential(n, e):
    return n ** e

def multiplication(a, b):
    return a * b

def division(a, b):
    if b != 0:
        return a / b
    else:
        return 'Error: Cannot divide by 0'

def modulus(a, b):
    if b != 0:
        return a % b
    else:
        return 'Error: Cannot divide by 0'

def f_division(a, b):
    if b != 0:
        return a // b
    else:
        return 'Error: Cannot divide by 0'

def addition(a, b):
    return a + b

def subtraction(a, b):
    return a - b
